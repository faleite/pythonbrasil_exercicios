""" 2. Faça um Programa que peça um valor e mostre na tela
se o valor é positivo ou negativo.
"""

valor = float(input('Insira um valor: '))

if valor >= 0:
    print('Este valor é positivo!')
else:
    print('Este valor é negativo!')


